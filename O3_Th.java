package com.company;

public class O3_Th {
    public static void main(String[] args) {
        int a = 4;
        int b = 6 % a; // Module Operator used to find out reminder
        a *= 3;
        System.out.println(a);
        System.out.println(b);
        System.out.println(a==a);
        System.out.println(10==10);
        System.out.println(10==01);
        System.out.println(10>01);
        System.out.println(64>5 && 64>8);
        System.out.println(6>45 && 64>8);
        System.out.println(6>45 || 64>8);
    }
}
